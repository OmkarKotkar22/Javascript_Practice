let str = "I Love Javascript";

//toUpperCase()
// Returns the string converted to uppercase letters
console.log(str.toUpperCase());
// Output: "I LOVE JAVASCRIPT"

//toLowerCase()
// Returns the string converted to lowercase letters
console.log(str.toLowerCase());
// Output: "i love javascript"


//toLocaleUpperCase()
// Returns the string converted to uppercase letters according to any locale-specific case mappings
console.log(str.toLocaleUpperCase());
// Output: "I LOVE JAVASCRIPT"

//toLocaleLowerCase()
// Returns the string converted to lowercase letters according to any locale-specific case mappings
console.log(str.toLocaleLowerCase());
// Output: "i love javascript"


